package com.devopsbuddy.devopsbuddy.service.user;

import com.devopsbuddy.devopsbuddy.repository.UserRepository;
import com.devopsbuddy.devopsbuddy.web.controller.domain.frontend.User;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

@Service
public class UserDetailsServiceImpl implements UserDetailsService {
 private static final Logger LOG = LoggerFactory.getLogger(UserDetailsServiceImpl.class);

    @Autowired
    UserRepository userRepository;

    @Override
    public UserDetails loadUserByUsername(String userName) throws UsernameNotFoundException {
      User user =  userRepository.findUserByUserName(userName);
      if(user==null)
      {
         LOG.warn("User not found {}",user);
         throw new UsernameNotFoundException("User "+userName + " not found!");
      }
      LOG.debug("User with name:",userName);
      return user;
    }
}









































































































































































































































































































































































































































































































































































































































































































































































































































































































